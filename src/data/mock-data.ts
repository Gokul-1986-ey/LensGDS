function q(
  id: string,
  insightId: string,
  questionText: string,
  answerType: 'yes-no' | 'select-one' | 'numeric' | 'slider' | 'free-text',
  category: 'confirming' | 'quantifying' | 'exploring' | 'challenging',
  options: string[] | undefined,
  affectedTopicIds: string[]
) {
  return {
    id,
    insightId,
    questionText,
    answerType,
    options,
    category,
    affectedTopicIds,
    impactMapping: {
      confirmDelta: category === 'confirming' ? 0.15 : 0.1,
      contradictDelta: category === 'confirming' ? -0.2 : -0.15,
      unknownDelta: -0.05,
    },
  } as import('@/types/domain').ValidationQuestion;
}

